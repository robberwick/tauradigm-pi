def trim_path(path):
    point = 1
    while (point < (len(path)-1)):
        dy = abs(path[point][0] - path[point-1][0])
        dx = abs(path[point][1] - path[point+1][1])
        #print(f'dx: {dx}, dy: {dy}')
        if (dx == 1 and dy == 1):
          del path[point]
        point += 1
    return path

